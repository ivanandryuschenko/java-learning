package refactor;

public class Position {
    private int x;
    private int y;

    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position sum(Position p) {
        return new Position(x + p.getX(), y + p.getY());
    }
}
